Date.prototype.toYNABDate = function() {

    var y = this.getFullYear().toString();
    var m = (this.getMonth() + 1).toString();

    //Prepend a zero if needed.
    if(m.length == 1) { m = "0" + m; }

    return y + "-" + m + "-01";

};